

class Address{
	private String street;
	private char number;
	
	public Address(){
	}
	
	public void setStreet(String street){
		this.street=street;
	}
	
	public String getStreet(){
		return street;
	}
	
	public void setNumber(char number){
		this.number=number;
	}
	
	public char getNumber(){
		return number;
	}
};
